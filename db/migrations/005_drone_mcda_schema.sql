-- =============================================================================
--  DRONE AIRSPACE ZONING PLATFORM — DATABASE SCHEMA (MVP, Region 4 pilot)
-- =============================================================================
--  Target: PostgreSQL 15+ with PostGIS. Written for Supabase; portable to
--          Neon and Railway unchanged (all standard Postgres + PostGIS).
--
--  DESIGN COMMITMENTS ENCODED HERE (settled in planning):
--    * Two-stage logic: hard CONSTRAINTS first, then weighted SUITABILITY.
--    * SIX AHP-weighted factors (matches Drone-2 §3.4.1 methodology).
--    * THREE-LEVEL scoring hierarchy:
--         L1 Factor    -> carries the AHP weight            (mcda_factors)
--         L2 Sub-type  -> default 1-5 score / buffer / zone (mcda_subtypes)
--         L3 Feature   -> per-feature attribute override     (mcda_feature_rules)
--      Cascade: L3 override -> else L2 default. Weight (L1) always applies.
--    * 4-tier zoning: PROHIBITED / RESTRICTED / CONDITIONAL / SUITABLE.
--    * Precedence: MOST-RESTRICTIVE-WINS (both for constraint overlap and for
--      multiple sub-types touching one cell -> take MAX score).
--    * H3 hexagon grid, resolution 9 (per-cell reason retention).
--    * CRS: store 4326, BUFFER in UTM 21N (EPSG:32621), serve 3857/4326.
--    * Weights are RENORMALISED AT RUN TIME (weight_i / sum(weights)), so any
--      positive weights run without breaking — this is what lets you tweak
--      weights / do +/-10% sensitivity analysis without the program failing.
--    * Every input layer carries a CONFIDENCE tag for the location report.
-- =============================================================================


-- -----------------------------------------------------------------------------
-- 0. EXTENSIONS
-- -----------------------------------------------------------------------------
CREATE EXTENSION IF NOT EXISTS postgis;
-- H3 extensions are OPTIONAL. On Supabase they exist and enable in-database hex
-- generation; elsewhere (Neon, Railway, vanilla Postgres) the Python `h3` library
-- in ingest.py generates the grid instead. Schema stores h3_index as TEXT + geom
-- either way, so nothing downstream depends on the extension.
DO $$ BEGIN
    CREATE EXTENSION IF NOT EXISTS h3;
    CREATE EXTENSION IF NOT EXISTS h3_postgis;
EXCEPTION WHEN OTHERS THEN
    RAISE NOTICE 'h3 extensions unavailable — grid will be generated by ingest.py (fine).';
END $$;


-- -----------------------------------------------------------------------------
-- 1. ENUMS / TYPES
-- -----------------------------------------------------------------------------
-- Zone order encodes severity (most severe first). Used for most-restrictive-wins.
CREATE TYPE drone_zone AS ENUM (
    'PROHIBITED',   -- No-fly. Hard constraint.
    'RESTRICTED',   -- Authorization required.
    'CONDITIONAL',  -- Caution / mitigation.
    'SUITABLE'      -- Lower-risk, normal rules.
);

-- How a sub-type participates in the model.
CREATE TYPE mcda_treatment AS ENUM (
    'constraint',   -- Hard rule: buffer -> locked output zone.
    'factor'        -- Weighted suitability: contributes a 1-5 score.
);

-- Data trustworthiness, surfaced in the location report.
CREATE TYPE data_confidence AS ENUM (
    'verified',     -- From a named authoritative agency (e.g. Bureau of Statistics).
    'open_data',    -- Reputable open dataset (e.g. Protected Planet).
    'proxy_osm',    -- OpenStreetMap / crowd-sourced fill.
    'digitized',    -- Hand-digitized / interpretive (e.g. some GuyNode layers).
    'unverified'    -- Placeholder / incomplete; treat as provisional.
);


-- shared updated_at trigger
CREATE OR REPLACE FUNCTION set_updated_at() RETURNS trigger AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;


