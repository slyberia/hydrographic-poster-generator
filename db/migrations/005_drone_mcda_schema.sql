-- -----------------------------------------------------------------------------
-- 5. LEVEL 3 — FEATURE RULES (per-feature attribute overrides of L2 defaults)
--    Handles BOTH categorical refinements (size_class='large') and numeric
--    banding (population density value_min..value_max). Falls back to L2 when
--    no rule matches — this is the graceful cascade.
-- -----------------------------------------------------------------------------
CREATE TABLE mcda_feature_rules (
    feature_rule_id SERIAL PRIMARY KEY,
    subtype_id      INTEGER NOT NULL REFERENCES mcda_subtypes(subtype_id) ON DELETE CASCADE,
    match_attribute TEXT NOT NULL,               -- feature attribute inspected, e.g. 'size_class','density_per_km2','aerodrome_class'
    match_value     TEXT,                          -- categorical exact match (nullable)
    value_min       NUMERIC,                       -- numeric range low  (inclusive, nullable)
    value_max       NUMERIC,                       -- numeric range high (exclusive, nullable)
    -- Overrides (any NULL falls back to the sub-type default):
    score           SMALLINT,
    buffer_m        NUMERIC,
    output_zone     drone_zone,
    reason_template TEXT,
    source          TEXT,
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT l3_score_range CHECK (score IS NULL OR score BETWEEN 1 AND 5),
    CONSTRAINT l3_match_present CHECK (match_value IS NOT NULL OR value_min IS NOT NULL OR value_max IS NOT NULL)
);
CREATE INDEX idx_feature_rules_subtype ON mcda_feature_rules(subtype_id);

