-- Verification view: shows raw weights, the renormalised weights the engine will
-- actually use, and their sum. Because of run-time renormalisation, the raw sum
-- does NOT need to equal 1.0 — this view is a sanity aid, not a constraint.
CREATE VIEW mcda_weight_check AS
SELECT
    factor_key,
    factor_name,
    weight AS raw_weight,
    ROUND(weight / NULLIF(SUM(weight) FILTER (WHERE is_active) OVER (), 0), 4) AS normalised_weight,
    is_active
FROM mcda_factors
ORDER BY factor_key;

-- =============================================================================
--  NOTES FOR THE ENGINE (implemented in FastAPI, not in this DDL)
-- =============================================================================
--  SCORE RESOLUTION per (feature, cell):
--    1. If an active mcda_feature_rules row matches the feature's attribute
--       (categorical match_value OR numeric value_min<=v<value_max) -> use its
--       non-NULL overrides.
--    2. Else fall back to the sub-type (L2) defaults.
--    3. Multiple sub-types touching one cell within a factor -> MAX score
--       (most-restrictive-wins).
--  FACTOR AGGREGATION per cell:
--    total = SUM(factor_score_i * normalised_weight_i) over active factors,
--    where normalised_weight_i = raw_weight_i / SUM(active raw weights).
--    -> Renormalisation is why editing weights / +/-10% sensitivity never breaks.
